package learnsomejava;

class Learn {

  int addOneToInt(int numberToAddTo) {

    int result = numberToAddTo + 1;

    // Add 1 to result
    return result;
  }

  double addHalfToDouble(double numberToAddTo) {

    double result = numberToAddTo + 0.5d;

    // Add 0.5 to result
    return result;
  }
}